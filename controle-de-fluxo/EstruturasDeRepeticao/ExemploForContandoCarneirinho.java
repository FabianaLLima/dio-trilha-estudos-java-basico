//For: É uma estrutura de repetição que permite que variavael contadora seja testada e incrementada a cada interação.
public class ExemploForContandoCarneirinho {
	public static void main(String[] args) {
		for(int carneirinhos = 1 ; carneirinhos <=20; carneirinhos ++) {
			System.out.println("Contando carneirinhos" + carneirinhos);
		}
	}
}